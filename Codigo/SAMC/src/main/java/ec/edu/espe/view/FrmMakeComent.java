    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        String comment = txtComment.getText();

        if (comment == null || comment.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingresa un comentario antes de enviarlo.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        MongoClient client = MongoDBManager.getMongoClient();
        if (client == null) {
            JOptionPane.showMessageDialog(this, "No se pudo conectar a MongoDB", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        MongoDatabase database = client.getDatabase("SAMC");
        MongoCollection<Document> collection = database.getCollection("comentarios");

        Document comentarioDoc = new Document()
                .append("comentario", comment)
                .append("fecha", new java.util.Date());

        try {
            collection.insertOne(comentarioDoc);
            JOptionPane.showMessageDialog(this, "Comentario enviado exitosamente.", "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);
            txtComment.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al enviar el comentario: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

